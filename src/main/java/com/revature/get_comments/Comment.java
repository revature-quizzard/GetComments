package com.revature.get_comments;

import com.amazonaws.services.dynamodbv2.datamodeling.*;
import lombok.Data;
import lombok.NoArgsConstructor;

import java.time.LocalDateTime;
import java.util.List;

    @Data
    @NoArgsConstructor
    @DynamoDBTable(tableName = "ForumNodes")
    public class Comment {

        @DynamoDBHashKey
        @DynamoDBAutoGeneratedKey
        private String id;

        @DynamoDBAttribute
        private String subject;

        @DynamoDBAttribute
        private List<String> ancestors;

        @DynamoDBAttribute
        private String parent;

        @DynamoDBAttribute
        private String description;

        @DynamoDBAttribute
        private int child_count;

        @DynamoDBAttribute
        private String date_created;

        @DynamoDBAttribute
        private String owner;

        @DynamoDBAttribute
        private List<String> tags;

        public Comment(String id, String subject, String description, List<String> ancestors) {
            this.id = id;
            this.subject = subject;
            this.ancestors = ancestors;
            this.parent = ancestors.get(1);
            this.description = description;
            this.tags = null;
            this.child_count = 0;
            this.date_created = LocalDateTime.now().toString();
        }
}
